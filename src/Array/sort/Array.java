package Array.sort;

public class Array {
    public static void main(String[] args) {
        int[] arr = {5, 3, 8, 4, 2};

        System.out.println("Исходный массив:");
        for (int num : arr) {
            System.out.print(num + " ");
        }



        System.out.println("\nОтсортированный массив:");
        for (int num : arr) {
            System.out.print(num + " ");
        }
    }
}
